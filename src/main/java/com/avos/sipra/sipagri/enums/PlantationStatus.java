package com.avos.sipra.sipagri.enums;

/**
 * Enumeration representing the various statuses of a plantation.
 * <p>
 * This enum provides constants to indicate whether a plantation
 * is active and operational or inactive and not currently in use.
 */
public enum PlantationStatus {
    /**
     * Indicates that a plantation is active and operational.
     * This status is used to denote that the plantation is
     * currently in use and functioning as expected.
     */
    ACTIVE, /**
     * Represents the inactive status of a plantation.
     * <p>
     * This status indicates that the plantation is not currently active
     * or operational, signifying that activities within the plantation
     * are on hold or have been discontinued.
     */
    INACTIVE
}
