package com.avos.sipra.sipagri.enums;

/**
 * Enumeration representing the gender of a human individual.
 * <p>
 * This enum defines the binary gender types applicable to humans: male and female.
 * It can be used in contexts where categorizing or identifying an individual's gender is required.
 */
public enum HumanGender {
    /**
     * Represents the male gender category of a human individual.
     * This value is used to denote male individuals in contexts
     * where gender-based categorization or identification is required.
     */
    MALE,
    /**
     * Represents the female gender as a classification of a human individual.
     * This value is a constant in the HumanGender enumeration.
     */
    FEMALE;
}
